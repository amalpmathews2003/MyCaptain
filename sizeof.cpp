#include <iostream>
using namespace std;
int main()
{
	char a='A';
	int b=1;
	float c=3.14;
	double d=4.6744;
	cout<<"Size of Char : "<<sizeof(a)<<" Byte\n";
	cout<<"Size of Int : "<<sizeof(b)<<" Byte\n";
	cout<<"Size of Float : "<<sizeof(c)<<" Byte\n";
	cout<<"Size of Double : "<<sizeof(d)<<" Byte\n";

	return 0;
}
